export function getGroups(channels: Channel[]): string[] {
  const groups = new Set<string>();
  channels.forEach(c => {
    c.group.split(',').forEach(g => groups.add(g.trim()));
  });
  return ['All', ...Array.from(groups).sort()];
}

export function filterByCategory(channels: Channel[], category: string): Channel[] {
  if (category === 'All') return channels;
  return channels.filter(c => c.group.toLowerCase().includes(category.toLowerCase()));
}

